
section .data

n: dd 6


str: db "Factorial of six is: ", 0x0a,0x00 
str_len: equ $-str
str1: db "Factorial is stored in r15D",0x0a,0x00
str_len1: equ $-str1
section .text
global _start
_start:
mov ebx,dword[n]


mov eax,0x01 

_loop:
imul eax,ebx
dec ebx
cmp ebx,0x00

jle _return
jmp _loop

_return:
mov r15D, eax
mov rax, 0x04
mov rbx, 0x01
lea rcx, [str]
mov rdx, str_len
int 0x80
mov rax, 0x04
mov rbx, 0x01
lea rcx, [str1]
mov rdx, str_len1
int 0x80
mov rax, 0x01
mov rbx, 0x00
int 0x80


































 
































































 

































 


 






















 







 


 

































































































































 



















